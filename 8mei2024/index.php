        <!-- PHP Script untuk menampilkan daftar file -->
        <?php
        // Direktori dari file-file yang ingin ditampilkan
        $dir = "./";
        
        // Mengambil daftar file dalam direktori
        $files = scandir($dir);
        
        // Menampilkan daftar file sebagai list
        foreach($files as $file) {
            // Memastikan file adalah file (bukan direktori) dan bukan file sistem
            if(is_file($dir . $file) && !in_array($file, array('.', '..'))) {
                // Membuat link untuk membuka file
                echo "<li><a href='$file'>$file</a></li>";
            }
        }
        ?>
